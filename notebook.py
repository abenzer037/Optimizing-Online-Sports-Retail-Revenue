# ## 8. Footwear product performance
# <p>Looks like product reviews are highest in the first quarter of the calendar year, so there is scope to run experiments aiming to increase the volume of reviews in the other nine months!</p>
# <p>So far, we have been primarily analyzing Adidas vs Nike products. Now, let's switch our attention to the type of products being sold. As there are no labels for product type, we will create a Common Table Expression (CTE) that filters <code>description</code> for keywords, then use the results to find out how much of the company's stock consists of footwear products and the median <code>revenue</code> generated by these items.</p>

# In[8]:


get_ipython().run_cell_magic('sql', '', "\n-- Create the footwear CTE, containing description and revenue\n-- Filter footwear for products with a description containing %shoe%, %trainer, or %foot%\n-- Also filter for products that are not missing values for description\n-- Calculate the number of products and median revenue for footwear products\nWITH footwear AS\n(\n    SELECT i.description, f.revenue\n    FROM info AS i\n    INNER JOIN finance AS f \n        ON i.product_id = f.product_id\n    WHERE i.description ILIKE '%shoe%'\n        OR i.description ILIKE '%trainer%'\n        OR i.description ILIKE '%foot%'\n        AND i.description IS NOT NULL\n)\n\nSELECT COUNT(*) AS num_footwear_products, \n    percentile_disc(0.5) WITHIN GROUP (ORDER BY revenue) AS median_footwear_revenue\nFROM footwear;")


