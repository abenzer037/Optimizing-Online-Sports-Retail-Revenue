# ## 3. Labeling price ranges
# <p>It turns out there are 77 unique prices for the products in our database, which makes the output of our last query quite difficult to analyze. </p>
# <p>Let's build on our previous query by assigning labels to different price ranges, grouping by <code>brand</code> and <code>label</code>. We will also include the total <code>revenue</code> for each price range and <code>brand</code>. </p>

# In[3]:


get_ipython().run_cell_magic('sql', '', "\n-- Select the brand, a count of all products in the finance table, and total revenue\n-- Create four labels for products based on their price range, aliasing as price_category\n-- Join brands to finance on product_id and filter out products missing a value for brand\n-- Group results by brand and price_category, sort by total_revenue\nSELECT b.brand, COUNT(f.*), SUM(f.revenue) as total_revenue,\nCASE WHEN f.listing_price < 42 THEN 'Budget'\n    WHEN f.listing_price >= 42 AND f.listing_price < 74 THEN 'Average'\n    WHEN f.listing_price >= 74 AND f.listing_price < 129 THEN 'Expensive'\n    ELSE 'Elite' END AS price_category\nFROM finance AS f\nINNER JOIN brands AS b \n    ON f.product_id = b.product_id\nWHERE b.brand IS NOT NULL\nGROUP BY b.brand, price_category\nORDER BY total_revenue DESC;")


